# Tuplas são estruturas de dados muito parecidas com listas. A principal diferença é que tuplas são imutáveis enquanto listas são mutáveis. Criamos tuplas usando a classe tuple ou colocando valores separados por vírgulas de parenteses.

# Exemplo:

frutas = ('laranja', 'pera', 'uva',)

letras = tuple('python')

numeros = tuple([1, 2, 3, 4])

pais = ('brasil',)

# De resto, trabalhamos com tuplas da mesma forma que trabalhamos com listas, sem diferença alguma.

print(frutas[0])
print(pais[0])
print(numeros[::-1])
