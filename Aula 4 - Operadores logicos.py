saldo = 1000
saque = 200
limite = 100

#Operador E:
#AMBOS PRECISAM SER VERDADEIROS PARA SER VERDADE.
print (saldo >= saque and saque <= limite)
#false

#Operador OU:
print (saldo >= saque or saque <= limite)
#true

#Operador NEGAÇÃO:
print(saldo != 200)
#Verifica se o saldo NÃO É IGUAL a 200.
#true

